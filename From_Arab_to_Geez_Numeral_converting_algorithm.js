ቀዳሜ();

function ቁጥሩ_ግእዝ(ቁጥሩ) {
    const ቁጥር_ሀረግ = ቁጥሩ.toString();
    const ብዛቱ = Math.ceil(ቁጥር_ሀረግ.length / 2);

    let ግእዝ_ቁ = '';
    let ተሌ = ቁጥር_ሀረግ;

    for (let ለ = 1; ለ <= ብዛቱ; ለ++) {
        // Generate multiplier symbol efficiently
        const ሙልቲፕላየር = ለ == 1
            ? ''
            : እልፎ[1].repeat(Math.ceil((ለ - 2) / 2)) + (ለ % 2 === 0 ? እልፎ[0] : '');

        const ነጠላቁ1 = ተሌ.slice(-1);
        const ነጠላቁ2 = ተሌ.length > 1 ? ተሌ.slice(-2, -1) : '0';

        let እልፍ = (ነጠላቁ1 === '0' && ነጠላቁ2 === '0') ? '' : ሙልቲፕላየር;
        ግእዝ_ቁ += እልፍ;

        if (!(ነጠላቁ1 === '0' || (ለ > 1 && ነጠላቁ2 === '0' && ነጠላቁ1 === '1'))) {
            ግእዝ_ቁ += አንዶ[parseInt(ነጠላቁ1) - 1];
        }

        if (ነጠላቁ2 !== '0') {
            ግእዝ_ቁ += አስሮ[parseInt(ነጠላቁ2) - 1];
        }

        ተሌ = ተሌ.slice(0, -2);
    }

    return ግእዝ_ቁ.split('').reverse().join('');
}
